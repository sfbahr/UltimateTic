package cs2114.ultimatetic;

/**
 *  This class enumerates the state of a cell on a tac board.
 *
 * @author Samuel Bahr (sfbahr)
 * @author Brian Clarke (golfboy1)
 * @author Charles Tenney (charten)
 * @version 2014.04.30
 */
public enum Cell
{
    /**
     * Cell is unoccupied.
     */
    EMPTY,
    /**
     * Cell is marked with red for player 1.
     */
    RED1,
    /**
     * Cell is marked with blue for player 2.
     */
    BLUE2;
}
